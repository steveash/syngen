package com.github.steveash.synthrec.service;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Marks a service implementation as a default; this is used interanlly by the
 * core toolkit to allow proprietary implementations to _override_ the default
 * toy implementations. You don't want to include these on your services; because
 * there should only be one default service per service type
 * @author Steve Ash
 */
@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.TYPE})
public @interface DefaultService {
}
